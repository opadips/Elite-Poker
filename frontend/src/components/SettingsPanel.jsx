function CardBackTooltipPreview({ backId, name }) {
  const patterns = {
    classic: { bg: '#d9a036', fg: '#c48a22' },
    royal: { bg: '#5e2a7a', fg: '#3d1a53' },
    emerald: { bg: '#1f5e3a', fg: '#144a2c' },
    sapphire: { bg: '#1e3a5f', fg: '#0f2740' },
    onyx: { bg: '#2a2a2a', fg: '#1a1a1a' },
    pearl: { bg: '#e8e0d5', fg: '#ccbca7' },
  };
  const p = patterns[backId] || patterns.classic;
  return (
    <div className="group relative inline-block">
      <div className="w-8 h-12 rounded shadow border border-white/20 overflow-hidden" style={{ backgroundColor: p.bg }}>
        <div className="h-full w-1/2 mx-auto" style={{ backgroundColor: p.fg, opacity: 0.4 }} />
      </div>
      <div className="pointer-events-none absolute -top-16 left-1/2 -translate-x-1/2 opacity-0 group-hover:opacity-100 transition-opacity duration-150 bg-gray-800 text-white text-xs rounded-lg px-3 py-2 shadow-xl whitespace-nowrap z-50">
        <div className="w-14 h-20 rounded shadow border border-white/20 overflow-hidden mx-auto mb-1" style={{ backgroundColor: p.bg }}>
          <div className="h-full w-1/2 mx-auto" style={{ backgroundColor: p.fg, opacity: 0.4 }} />
        </div>
        <div className="text-center">{name}</div>
      </div>
    </div>
  );
}

export default function SettingsPanel({
  showSettings,
  setShowSettings,
  theme,
  themes,
  onThemeChange,
  themeExpanded,
  setThemeExpanded,
  cardBackExpanded,
  setCardBackExpanded,
  cardBackOptions,
  cardBack,
  handleCardBackChange,
  seatViewFixed,
  toggleSeatView,
  soundEnabled,
  setSoundEnabled,
  showHandInfo,
  onToggleBeginner,
  isPaused,
  togglePause,
  isAdmin,
  setResetConfirm,
  requestHandHistory,
  onReturnToLobby,
  performanceMode,
  setPerformanceMode,
}) {
  if (!showSettings) return null;

  return (
    <div className="absolute top-full right-0 mt-1 w-80 bg-gray-900/95 backdrop-blur-md rounded-xl shadow-2xl border border-gray-700 z-50 overflow-hidden transition-all duration-200 origin-top-right scale-100 opacity-100"
         style={{ transformOrigin: 'top right' }}>
      <div className="max-h-[80vh] overflow-y-auto settings-scroll">
        <div className="px-5 py-3 bg-gray-800/50 border-b border-gray-700 flex items-center gap-2">
          <GearIcon /><span className="text-white font-bold text-sm">Settings</span>
        </div>
        <div className="border-b border-gray-700/50">
          <div className="px-4 py-3 flex justify-between items-center cursor-pointer hover:bg-gray-800/50" onClick={() => setThemeExpanded(!themeExpanded)}>
            <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider flex items-center gap-2"><PaletteIcon /> Theme</div>
            <span className="text-gray-400 text-sm">{themeExpanded ? '\u25B2' : '\u25BC'}</span>
          </div>
          {themeExpanded && (
            <div className="px-4 py-3 grid grid-cols-2 gap-3">
              {themes.map(t => (
                <button key={t.id} onClick={() => { onThemeChange(t.id); setShowSettings(false); }}
                  className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm transition-all ${theme === t.id ? 'bg-amber-500/20 text-amber-400 border border-amber-500/50 shadow-sm' : 'bg-gray-800 text-gray-300 hover:bg-gray-700 border border-transparent'}`}>
                  <ThemeTooltipPreview color={t.color} name={t.name} />
                  <span>{t.name}</span>
                </button>
              ))}
            </div>
          )}
        </div>
        <div className="border-b border-gray-700/50">
          <div className="px-4 py-3 flex justify-between items-center cursor-pointer hover:bg-gray-800/50" onClick={() => setCardBackExpanded(!cardBackExpanded)}>
            <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider flex items-center gap-2"><CardIcon /> Card Back</div>
            <span className="text-gray-400 text-sm">{cardBackExpanded ? '\u25B2' : '\u25BC'}</span>
          </div>
          {cardBackExpanded && (
            <div className="px-4 py-3 grid grid-cols-3 gap-3">
              {cardBackOptions.map(back => (
                <button key={back.id} onClick={() => handleCardBackChange(back.id)}
                  className={`flex flex-col items-center gap-2 p-2 rounded-lg transition-all ${cardBack === back.id ? 'bg-amber-500/20 border border-amber-500/50' : 'bg-gray-800 border border-transparent hover:bg-gray-700'}`}>
                  <CardBackTooltipPreview backId={back.id} name={back.name} />
                  <span className="text-xs text-gray-300">{back.name}</span>
                </button>
              ))}
            </div>
          )}
        </div>
        <div className="px-4 py-3 border-b border-gray-700/50">
          <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider mb-2 flex items-center gap-2"><MonitorIcon /> Seat View</div>
          <button onClick={toggleSeatView} className={`w-full flex items-center justify-between px-4 py-2 rounded-lg transition-all ${seatViewFixed ? 'bg-blue-500/20 text-blue-400 border border-blue-500/50' : 'bg-gray-800 text-gray-400 border border-transparent'}`}>
            <span className="text-sm font-medium">{seatViewFixed ? 'Fixed (My Seat Bottom)' : 'Dynamic (Rotating)'}</span>
            <span className="text-xs font-bold">{seatViewFixed ? 'FIX' : 'DYN'}</span>
          </button>
        </div>
        <div className="px-4 py-3 border-b border-gray-700/50">
          <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider mb-2 flex items-center gap-2">{soundEnabled ? <VolumeIcon /> : <MuteIcon />} Sound</div>
          <button onClick={() => setSoundEnabled(prev => !prev)} className={`w-full flex items-center justify-between px-4 py-2 rounded-lg transition-all ${soundEnabled ? 'bg-green-500/20 text-green-400 border border-green-500/50' : 'bg-gray-800 text-gray-400 border border-transparent'}`}>
            <span className="text-sm font-medium">{soundEnabled ? 'ON' : 'OFF'}</span><span className="text-xs font-bold">{soundEnabled ? 'ON' : 'OFF'}</span>
          </button>
        </div>
        <div className="px-4 py-3 border-b border-gray-700/50">
          <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider mb-2 flex items-center gap-2">{performanceMode ? <BoltIcon /> : <TurtleIcon />} Performance Mode</div>
          <button onClick={() => setPerformanceMode(prev => !prev)} className={`w-full flex items-center justify-between px-4 py-2 rounded-lg transition-all ${performanceMode ? 'bg-yellow-500/20 text-yellow-400 border border-yellow-500/50' : 'bg-gray-800 text-gray-400 border border-transparent'}`}>
            <span className="text-sm font-medium">{performanceMode ? 'Fast (Reduced animations)' : 'Full animations'}</span>
            <span className="text-xs font-bold">{performanceMode ? 'FAST' : 'FULL'}</span>
          </button>
        </div>
        <div className="px-4 py-3 border-b border-gray-700/50">
          <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider mb-2 flex items-center gap-2"><NoobIcon /> Noob Mode</div>
          <label className="flex items-center gap-2 cursor-pointer">
            <input type="checkbox" checked={showHandInfo} onChange={(e) => onToggleBeginner(e.target.checked)} className="w-4 h-4" />
            <span className="text-white text-sm">Noob Mode</span>
          </label>
        </div>
        <div className="px-4 py-3 border-b border-gray-700/50">
          <div className="text-gray-400 text-xs font-semibold uppercase tracking-wider mb-2 flex items-center gap-2">{isPaused ? <PlayIcon /> : <PauseIcon />} Game Control</div>
          <button onClick={togglePause} className={`w-full flex items-center justify-between px-4 py-2 rounded-lg transition-all ${isPaused ? 'bg-green-500/20 text-green-400 border border-green-500/50' : 'bg-yellow-500/20 text-yellow-400 border border-yellow-500/50'}`}>
            <span className="text-sm font-medium">{isPaused ? 'Resume' : 'Pause'}</span><span className="text-xs font-bold">{isPaused ? 'GO' : 'STOP'}</span>
          </button>
        </div>
        {isAdmin && (
          <div className="px-4 py-3 border-b border-gray-700/50">
            <button onClick={() => setResetConfirm(true)} className="w-full flex items-center justify-center gap-2 px-4 py-2 rounded-lg bg-red-500/20 text-red-400 border border-red-500/50 hover:bg-red-500/30 transition-all text-sm font-medium">
              <ResetIcon /> Reset Lobby
            </button>
          </div>
        )}
        <div className="px-4 py-3 border-b border-gray-700/50">
          <button onClick={requestHandHistory} className="w-full flex items-center justify-center gap-2 px-4 py-2 rounded-lg bg-blue-500/20 text-blue-400 border border-blue-500/50 hover:bg-blue-500/30 transition-all text-sm font-medium">
            <ScrollIcon /> Hand History
          </button>
        </div>
        <div className="px-4 py-3">
          <button onClick={onReturnToLobby} className="w-full flex items-center justify-center gap-2 px-4 py-2 rounded-lg bg-gray-700 text-gray-300 border border-gray-600 hover:bg-gray-600 transition-all text-sm font-medium">
            <DoorIcon /> Return to Lobby
          </button>
        </div>
      </div>
    </div>
  );
}